    private void optionOneActionPerformed(java.awt.event.ActionEvent evt) {
        jDialog2.setVisible(false);
        if(game.getDanger().isDeforestation()){
            game.getDanger().switchDeforestation(game.getPlayer(), 1);
        }else if(game.getDanger().isFire()){
            game.getDanger().switchcontrolFire(game.getPlayer(), 1);
        }else if(game.getDanger().isHunter()){
            game.getDanger().switcharrestHunter(game.getPlayer(), 1);
        }
        game.getmP().stop();
        game.getmP().startPlaying(game.getArrayTrack().get(5).getFilename());
        optionTextArea.setText("");
    }    
        
